def clean_generated(posts: list[dict]) -> None:
    for post in posts:
        target = BLOG_DIR / post["slug"]
        index = target / "index.html"
        if index.exists() and GENERATED_MARKER in index.read_text(encoding="utf-8", errors="ignore"):
            shutil.rmtree(target)


