def normalize_html_output(value: str) -> str:
    return (
        value.replace("\uca0c", "&middot;")
        .replace("\u0431\u0434", "&middot;")
        .replace("\u00b7", "&middot;")
        .replace("Scheduled ", "Published ")
    )


def clean_text(value: str) -> str:
    return html.unescape(re.sub(r"\s+", " ", re.sub(r"<[^>]+>", " ", value))).strip()


def extract_faq(body: str) -> list[dict]:
    marker = re.search(r"<h2[^>]*>\s*Brief FAQ\s*</h2>(.*?)(?:<h2|$)", body, flags=re.I | re.S)
    if not marker:
        return []
    section = marker.group(1)
    matches = re.findall(r"<h3[^>]*>(.*?)</h3>\s*<p[^>]*>(.*?)</p>", section, flags=re.I | re.S)
    return [
        {
            "@type": "Question",
            "name": clean_text(question),
            "acceptedAnswer": {"@type": "Answer", "text": clean_text(answer)},
        }
        for question, answer in matches
        if clean_text(question) and clean_text(answer)
    ]


